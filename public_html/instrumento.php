        <h1>Teclado</h1>
        <div class="esquerda" id="teclado_musical">
            <figure class="foto">
                <img class="fotos" src="_projeto_galeria/_yamaha/teclado.jpg">
                <figcaption>
                    <span class="titulos_fotos">Teclado Musical</span>
                    <p class="descricao_fotos">O Teclado é bem compacto, os modelos mais baratos são recomendados para quem está começando a aprender, os modelos mais caros são recomendados para bandas profissionais ou tecladistas que não estão mais no nível iniciante.</p>
                </figcaption>
            </figure>
        </div>

        <div class="direita">
            <div class="paragrafos" id="texto_teclado">
                <p class="texto">O teclado musical é muito mais compacto do que qualquer piano, ele é capaz de reproduzir vários sons e timbres diferentes, existem modelos mais simples e modelos mais profissionais, consequentemente a qualidade do som entre os diferentes modelos é bastante notável.</p>
                <p class="texto">Grande parte dos teclados possuem entrada para o pedal de <i>sustain</i>, entrada para fone de ouvido ou caixa de som externa, controlador de volume, entrada USB entre outras funções.</p>
                <p class="texto">Um defeito dos teclados é que as teclas são mais leves que a de um piano, logo, se acostumar com a leveza do teclado pode causar uma maior dificuldade ao se tocar em um piano.</p>
            </div> 
        </div>
        <h1>Pedais</h1>
        <div class="esquerda">
            <div class="paragrafos" id="texto_pedais">
                <p class="texto">Os pianos possuem dois ou três pedais. O pedal da direita é conhecido como <i>sustain</i>, ele prolonga o som das teclas, pois deixa elas soarem livremente.</p>
                <p class="texto">O pedal da esquerda é conhecido como <i>unacorda</i>, basicamente ele move os martelos levemente deixando o som mais suave.</p>
                <p class="texto">Por último temos o pedal central, conhecido como <i>sostenuto</i>. Nos pianos de cauda, ele prolonga o som das teclas que estão pressionadas quando ele foi ativado, deixando-as soar livremente, as próximas teclas pressionadas não surtirão o efeito. Os pianos verticais geralmente não possuem esse pedal, se possuir o efeito é diferente, simplesmente abafa o som das teclas.</p>
            </div>
        </div>

