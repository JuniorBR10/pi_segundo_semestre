        <h1>Piano</h1>
        <div class="esquerda">
            <div class="paragrafos" id="primeiro_texto">
                <p class="texto">O Piano acústico é um instrumento de teclas, pois o contato do humano com o instrumento é feito por meio delas, mas também é considerado um instrumento de cordas, porque quando as teclas são pressionadas elas acionam martelos que tocam nas cordas.</p>
                <p class="texto">Existem dois tipos de piano acústico, o de cauda, onde as cordas ficam dispostas na horizontal e o armário onde as cordas ficam dispostas na vertical.</p>
                <p class="texto">Com o avanço da tecnologia surgiram os pianos digitais, eles simulam o peso das teclas e os sons de um piano acústico, os pianos digitais podem ter as formas de piano de cauda e piano armário.</p>
